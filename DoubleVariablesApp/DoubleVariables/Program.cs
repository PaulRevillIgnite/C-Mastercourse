
// doubles can have a decimal point

double averageAge;

averageAge = (43 + 22 + 62) / 3;
Console.WriteLine(averageAge);

// Have to make it clear that this is a double calculation and not an int by adding a .0 to a number
averageAge = (43.0 + 22 + 62) / 3;
Console.WriteLine(averageAge);

// Notice how the number is rounded and doesn't go on forever
averageAge = 1.0 / 3;
Console.WriteLine(averageAge);